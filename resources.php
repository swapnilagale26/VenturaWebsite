          <div class="blogHeading">Reformatting Learning in the Modern Workplace</div>
          <div class="introImage"></div>

          <p>
          We live in the 21st Century!<br><br>

          How often is this used as a catchphrase to emphasise changes, technological advances, changing demographic in workplaces, the transition in organizational learning goals, the shifting priorities and most of all the way in which devices have become an integral part of the day to day functions. As the workplace evolves with time, so does the learning/training strategy. The traditional ways of workplace learning no longer fit the frame and need reformatting to meet the needs of the modern workplace. 


         Jane Hart, the Founder of the Centre for Learning & Performance Technologies, <a style="color: blue" target="_blank" href="http://www.c4lpt.co.uk/blog/2016/01/02/2016-rethinking-workplace-learning/">summarises learning in the modern workplace</a>, as a mindset that recognises learning as a part of the normal workflow, a mindset that appreciates the fact that learners learn for themselves, as a mindset that values workplace learning in all forms, one that realises that it is not just about tracking and reporting and that understands that the world is changing and that too at a fast pace.<br><br>

          So, what does the L&D (learning and development) department do now?<br><br>

          While there is still a lot on their plate, workplace learning is no longer the sole responsibility of L&D. It has been reformatted and has now become a more holistic endeavour where the learners, managers, L&D team, all those who are part of the daily interactions have a role to play. Workplace learning is now more of a socio-collaborative activity, wherein eLearning plays the supportive role by sharing inputs as and where required. <br><br>

          Learners now take initiative to learn and share knowledge which in turn is supported at an organisational level by cultivating and open culture. This works in form of knowledge sharing portals, discussion boards, chats etc. Social media too plays an important role as it connects more easily with the modern learners, act as channel where they can effortlessly discuss and share content in many formats. <br><br>

         The role of managers and L&D team has changed from that of learning management to a more motivational category. Apart from planning training schedules and such, they now have to create an environment that supports learning. This happens not just through pep talks, but by putting flexible policies in place that make the learners feel more at ease. From encouraging a collaborative solution finding mindset to building a team to handle social learning and knowledge collaboration to creating the trend self-paced learning across the organisation, the role of L&D managers and even team leads needs a versatile twist. <br><br>
        
         The L&D’s key roles now needs a shift from just learning creation to that of learning innovation. They need to device ways to utilize the benefits of mobile learning, incorporate microlearning and performance support tools in the learning strategy. Transform learning from a face-to-face trainer driven process to a more.  <br><br>

         The transition from traditional workplace learning to modern workplace learning begins in small groups. Teams that promote queries, discussions in a professional environment, with channels that allow communication across the entire organisation and eLearning support to find information that is needed at the point of need.<br><br>

         Modern workplace is all about such flexibility, making employees feel at home to increase productivity, retention etc. and learning here is all about the seamless experience of interaction, communication, sharing, understanding and implementing.
        </p>

