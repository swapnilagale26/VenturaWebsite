          <div class="introImage"></div>

          <p>
          There is no doubt that Flash has been replaced by HTML5. That too, not just in web-based applications but in eLearning too. Various convertors, eLearning authoring tools and specialised eLearning vendors have gained popularity because of the sudden surge in Flash to HTML5 conversion. While the benefits of switching to Flash are multi-fold, most organisations/companies expect to see the benefits in terms of ROI or Cost Vs. Benefits. <br><br>

          Well, eLearning has different parameters for measuring the benefits and hence cannot always be equated in terms of monetary value, and hence the popular cost-benefit analysis tools can be of little use. Instead, we can compare on aspect that are visible and can be charted. <br><br>

          <strong>Development Cost</strong><br>
          There is a certain amount of cost associated with converting Flash based eLearning to HTML5. First off all it involves collection of all the available content, analysing the learning requirements, extracting the content from Flash etc. requires resources, time, expertise, additional assets for HTML5 conversion etc. This also includes the actual development cost i.e. of instructional designer, developer, authoring tool etc. <br><br>

