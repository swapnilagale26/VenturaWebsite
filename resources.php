          <div class="blogHeading">Tips for L&D Managers to Calculate ROI on eLearning</div>
          <div class="introImage"></div>
          <p>Profits, Turnaround etc. are terms associated with most businesses, and the same applies for investments in eLearning. As mentioned in the eBook- Calculating eLearning ROI In A Corporate Set-Up Where L&D Is A Support Function, “Quantifying and calculating eLearning ROI in a corporate set-up where L&D is a support function, may seem easy. After all it is about calculating the value of the total cost of investment by adding the costs incurred during various stages of developing eLearning course.” </p><br><br>

          <p>The role of L&D managers is crucial when it comes to such calculations. While various parameters influence the ROI, a few tips can come in handy when L&D managers have to calculate the ROI on eLearning.  <br><br>


          <ul>
            <li>Tip 1# Don’t Misinterpret <br> The market usually runs on the basis of comparison. However, when it comes to calculating ROI this may lead to misinterpretations. Each investment has a different form of cost and returns, and this differs for every organisation, every eLearning type, the learning strategy, the learning group etc. ROI is affected by these and hence the calculation should account for these too.</li><br><br>
            <li>Tip2# Consider the Time Duration <br> ROI calculation in eLearning is time sensitive. The returns usually take time to manifest and so, wait for a certain time gap after the eLearning is delivered. Also, as for other businesses, the results of eLearning need not always manifest as monetary growth, as it focuses more on filling skill-gap, bringing in behaviour change and making learners proactive, the returns in terms of profit and turn-around can be calculated only after a certain time. </li><br><br>
            <li>Tip3# Account for other Parameters<br> While calculating ROI on eLearning, L&D managers should also consider the various other parameters, including the market fluctuations, changes at the work, or overall work pressure on the learners. All these parameters play key role in how effective the eLearning turns out to be.</li>
          </ul><br><br>
          <p>While L&D Managers usually opt for eLearning considering all the cost-benefit aspects, the down-time requirements, and even the additional infrastructural requirements. Understanding that measuring the results and returns accurately requires accounting for various aspects that influence the learning progression is the key to its success. The Kirkpatrick’s learning evaluation model, that accounts for Learner’s Reaction, Learning Results, Learner Behaviour and Business Results is by far the most comprehensive evaluation model used in the industry. The Phillips ROI Methodology on the other hand calculates the Cost-Benefit Ration and ROI in more quantitative terms. A combination of both can hence give a clearer picture about how effective or ineffective a learning strategy or eLearning investment really is. <br><br>

          
