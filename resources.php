          <div class="blogHeading">Behavioural Learning vs. Cognitive Learning</div>
          <div class="introImage"></div>
          <p>Learning theories are the foundation of effective learning implementations. The learning content creation is the result of application of learning theories to deliver best learning experiences to the learners, a part usually handled by the instructional designers. There are 3 schools of thought otherwise known as learning theories: Behavioural Learning, Cognitive Learning and Constructive Learning, and like with all theories, more than often there are debates, discussions as on which is better. </p><br><br>

          <p>
          Here we shall discuss the first two, i.e. behavioural learning vs. cognitive learning. <br><br>

          <strong>Behavioural Learning </strong>as the title suggests, considers learning as a behaviour. Learning here is an entity that can be measured in terms of behavioural change. The theory originates from B.F. Skinner’s study about the concept of operant conditioning, a concept that states that learning occurs through rewards and punishments for behaviour. <br><br>

          
          Behavioural Learning hence is basically any change in behaviour due to the environmental changes or stimuli. The learning begins when the learners starts behaving or responding to a stimulus in a particular manner and that is repeated over and over under similar conditions. Basically, it is the result, reward or consequence that triggers the behaviour or action. Instructors often use Behaviourism when they reward learner behaviour.<br><br>

          Applications of Behavioural learning is often seen in Reinforced Learning, Compliance Training (where rules are established), Gamification etc. <br><br>
        
          Behavioural Learning however lacks in terms of problem solving and creativity. Learners fail to act in response to new situations or change actions accordingly. The only situations they are prepared for are based on basic facts and the responses associated to those.<br><br>

          Moving on to <strong>Cognitive Learning.</strong> The credit for Cognitive learning theories goes to Jean Piaget. Theoretically speaking learning here is the result of processing the information received, rather than just responding to stimuli. Thinking or thought process is the key here. The learning here is also manifested as behaviour change but it isn’t the only indicator of the process. The actual skills and knowledge are stored in the learner’s mind.<br><br>

          The stored knowledge is used when the learner sets out to understand and process new information that (s)he receives and then correlates it with the existing understanding/knowledge stored in the brain.  Unlike Behavioural learning, cognitive learning is an internal process and isn’t merely triggered by an external stimulus. Rather than observable behaviour, learning through thought analysis etc. are given importance. Change in behaviour is recorded as a shift in thought. The actual learning according to the cognitive model is the result of actual experiences.  <br><br>

          Applications of cognitive learning is seen in microlearning, branched scenario-based learning, real world examples, collaborative learning etc. The learning environment plays less important role here. <br><br>

          Behavioural learning may fade out due to lack of response to stimuli over time, this hence requires regular revisions. Cognitive learning on the other hand can be recalled easily depending on the way it is compartmentalised or chunked. <br><br>

          While both the learning theories have its own perks, it’s a blend of both that works best in the modern learning scenario. <br><br>

          So, which method of learning would you opt for? Do share your thoughts. 


