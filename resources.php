          Learners today appreciate the use of visuals instead of text and images are touted to be more powerful in terms of communicating the right message. However, a bit of description can add value to the just images. Infographics, videos on the other hand speak for themselves, and require much less additional (in this case textual) inputs.<br><br>

          <strong>Avoid Cluttering</strong><br>
          As a learner, having so many things to focus on can be a bit overwhelming and confusing too. Designing for the learners should be about organising information sequentially, broken onto easily digestible chunks, and arranged so that the whole learning can be processed, understood and retained. It should be almost similar to telling a story, starting from the end wouldn’t work always. And in case of learning content reverse engineering may not be the best way about, unless it is about assembling a product (you get the gist right).<br><br>

