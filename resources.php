          <div class="blogHeading">Learning Experience Designer VS Instructional Designer</div>
          <div class="introImage"></div>

          <p>
          Instructional Designer! Even newbies in the eLearning Industry must be well acquainted with this term. We know them as the brains behind eLearning, the ones who give life to most eLearning courses. But what do they do really? <br><br>

          Instructional Designer is the one who follow the practices of instructional design to create eLearning. So, then what does a Learning Experience Designer do? How do the profiles differ? <br><br>

          Let’s explore a bit.<br><br>

          <strong>Instructional Designer</strong>
          According to Wikipedia, “Instructional design (ID), or instructional systems design (ISD), is the practice of creating instructional experiences which make the acquisition of knowledge and skill more efficient, effective, and appealing. There are many instructional design models, but many are based on the ADDIE model with the five phases: analysis, design, development, implementation, and evaluation.” By definition an instructional designer focuses on creating instructional experiences. The emphasis here is mostly on the instruction and the instructor (or trainer, teacher, facilitator), the mode of instruction. While engagement aspects are given priority the whole strategy usually revolves around the organisation’s learning environment.<br><br>

          Instructional Designers focus on the behavioral and cognitive psychology. They usually follow a system, a rule-based approach and select the technology that fits the content delivery requirement.<br><br>

          Instructional design has been around for decades and has evolved over the time, whereas Learning Experience Design is a relatively new concept. With the changing times however, the role of instructional designers too has greatly changed. As  <a style="color: blue" href="https://elearningindustry.com/gaze-into-the-future-of-learning-with-connie-malamed" target="_blank"> Connie Malamed </a> once said, “in the future, Instructional Designers, in addition to creating courses for a specific user profile, would also begin to play a crucial role in enabling learning via online communities, social media technologies, curating content, and providing guidance to faculty and teaching experts on how to generate more relevant content.” This couldn’t be truer. Instructional designers now have to look at the broader picture, UX design and learner experience included, more or less overlapping with the profile of Learning Experience designers.<br><br>
        
          To understand that we have to know what the Learning experience designers do differently.<br><br>

          <strong>Learning Experience Designer</strong>
          While some experts define learning experience design as a new form of Instructional design. The experts at learningexperiencedesign.com, strongly argue that, it is quite different. According to them, “Learning experience design is the process of creating learning experience that enable the learner to achieve the desired learning outcome in a human centered and goal-oriented way. Learning experience designers always put the learner in the center of the experience. This results in a different design where the needs and preferences of the learner are leading.”<br><br>


          In other words, learning experience designers focus on creating an experience in a holistic manner. It covers both Instructional design and user experience design and the principles and models used here mostly focus on creating learning that is engaging, relevant and informative. The approach begins and ends with the users/learners/target audience, in reference with the learning environment, this makes it different from instructional design. <br><br>

          Also Known as the Learning Architects or Learning Designers, learning experience designers work to create cohesive learning. According to <a style="color: blue" href="https://elearningindustry.com/gaze-into-the-future-of-learning-with-connie-malamed" target="_blank"> Connie Malamed</a>, “Learning Experience Design implies a user-centric and holistic approach to solutions. It is a focus on experience and context more than materials. This is not just a change in title, but a change in how we perceive our mission, responsibility and practices. In addition, learning experience designers are thinking about ways to measure and evaluate performance in the workplace using data analytics. There is new interest in revealing valuable data about: activities that can be tracked, the best approaches for changing behavior, preventable errors, and so much more.” <br><br>

          As discussed above, though Instructional designers and Learning experience designers are experiencing an overlap in their roles mostly due to the varied learning requirements in the 21st century, both roles are distinct in their own ways. While defining the distinction precisely may be difficult, the actual difference can only be analysed in practice. <br><br>

          So, how would you define the role within the scope of your organisation? Do share your thoughts. 
        </p>

