          <div class="blogHeading">Designing Challenges in Responsive Learning Development</div>
          <div class="introImage"></div>
          <p>Tilt that phone, swipe that content, click the next button- different devices, different actions, different experiences, and how? Responsive Content! Works well for us right, as we switch from one device to another? The YouTube content, most websites, and even eLearning content giving us a seamless experience even when we change those screen sizes, operating systems. Ever wondered how it works? The magic is nothing but the work of Responsive Development. In terms of eLearning (briefly explained) Responsive Learning development creates a single course/project that works well both on Mobile devices and desktops- delivering an optimal experience on both. 
          However, it is easier said than done. The design process requires proper planning to create consistent learning experience across all the target devices. Here are some key designing challenges in Responsive Learning Development.</p><br><br>

          <p><strong>Visualisation</strong><br>
          As shown in the image above, the same eLearning content has to be rendered in different ways on different screens. The challenge here is to design elements, and focus on how it can flow from one screen dimension to another. While Responsive design uses Breakpoints to determine when the format of rendering changes, responsive sketches are required to understand how the pages would look on each device screen which is then implemented using HTML and CSS scripts.<br><br>

          <strong>Navigation</strong><br>
          While the design and position of common navigation buttons are universal, with customisations and creative designing things can become a bit complex. The challenge lies in creating navigation components that are intuitive and self-explanatory. The answer lies in analysing thoroughly the context of learning, i.e. how and when the learning take place, what is the key purpose etc. This helps in creating unique navigation that suits the context. This is the reason why the multi-layered drop-down menus on Desktop version are often replaced with Accordion design on Mobile devices.<br><br>

          <strong>Readability</strong><br>
          While this may seem simple, it still can cause accessibility issues. Imagine text in italics or ones that use colours that affect readability. The various ways to do so in responsive design include the use of ‘em’ units, Percent (%) as CSS font etc. Legibility and readability are the keys to engagement and capturing attention. The font type too plays an essential role in this. While Sans serif fonts have often been the choice, it is advisable to use versatile fonts similar (open source Google fonts) such as Open Sans, Lato, Old Standard TT, Abril Fatface, PT Serif etc. or it is advisable to use Font pairing to find the right combinations using tools like Google Type, Type Connection and Font Pair etc.<br><br>

          <strong>Image Rendering</strong><br>
          What would happen if an image designed for mobile devices were scaled to be rendered on a desktop screen? This should not be the case when content is repurposed in responsive eLearning. In many cases images are used to send the central message which is why this becomes even more important. Making images and icons scalable by using Scripts, SVG and icon fonts, optimizing the rendering through Lazy loading etc. are some tested solutions. <br><br>

          The key challenge however lies in developing a rich Responsive Learning experience that is affected by loading time, web-traffic (for browser based content), slow connectivity etc. Optimised content, conditional loading, and very often a mobile-first approach helps in developing and delivering a great responsive learning experience. </p>

