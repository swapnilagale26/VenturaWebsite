          <div class="blogHeading">How Well-designed Illustrations can help storytelling in eLearning</div>
          <div class="introImage" ></div>

          <p>
          ‘Once upon the time’ or ‘Long-long ago’, most of us grew up reading at least a couple of stories that began with those standard lines. Gradually the once non-illustrated books started becoming less popular and illustrated stories took its place. The most millennials will agree that life would have been quite different without Marvel and DC’s illustrative comics. Something similar works for eLearning too. <br><br>

          Illustrations (and media) often play a crucial role in enhancing what we call the user experience, engagement and even learning and retention. Where does storytelling fit in? Storytelling has often been considered a good way to introduce complex learning concepts and allows the learners to learn in stages as the story progresses. Goes without saying that illustrations can add the quality of storytelling, with images allowing the learners to visualize the entire scenario. <br><br>

          Listed below are other ways in which well-designed illustrations help storytelling in eLearning. <br><br>

          <strong>Instantly Connects with Learners</strong>
          A good illustration always leaves a good impression. This works for illustrative storytelling in eLearning too. Using illustration styles that the user group can connect with better (caricatures, cartoons etc.). Take for instance, a story-based eLearning module for using a newly acquired machinery. Illustrations definitely help the learners identify it better, and also in understanding the functionalities. <br><br>


          <strong>Makes Concepts Simpler</strong>
          The scenarios or situations within the stories used in eLearning often break down the concepts into simpler versions. When supported by relevant and contextual illustrations, the stories make more sense. Abstract concepts often make more sense when presented alongside with visual examples. Illustrations also assist in giving a quantitative representation to unquantifiable concepts, creating a level playing ground for comparison, pattern analysis etc. <br><br>

          <strong>Makes Learning Engaging</strong>
          Image/illustration has its own perks. A story if entirely textual can end up being boring too, however with the mix of the right amount of illustration it becomes more interesting and can keep the learners engaged for longer. Retention is also said to comparatively improve with the incorporation of right illustrations within the eLearning. <br><br>

          Illustrated Story telling model can be used to create infographics, or as videos with no voice-overs- i.e. only images and music, or as a series of comic-book-style drawings, still or in motion all to tell stories with little or no text at all. Add to those a bit of interaction and the learners have actions to perform and outcomes to look forward to.  <br><br>

          Utilization of globally accepted signs and symbols as illustrations, can assist in addressing multi-lingual learners without going back for translation each time a new learner group is added to the learning circle. This also works quite well in areas with low-literacy. Illustrative stories can send out the information just as well as explanative video-based modules, as long as the illustrations are well-designed. <br><br>
        
          The key however, lies in balancing between the right amount of text and the right amount of illustrations to bring out the best from storytelling.
        </p>

