          <div class="blogHeading">Challenges in HTML5 Development (And Resolutions)</div>
          <div class="introImage"></div>

          <p>
          HTML5 (<i>or Hyper Text Markup Language 5, if you call it so</i>) has been the talk of the town even before the demise of Flash, more in the eLearning circle if one must add. The whole multi device boom added to the growing popularity and suddenly HTML5 became the next big thing. HTML5 offers a cost-effective multi-platform development, delivers consistent output across multiple browser, offers offline browsing and better use of mobile features. If that doesn’t make it a good choice enough, eLearning authoring tools too offer an HTML5 output making it a crowd favourite amongst L&D professionals. <br><br>

          But, as with any other development language, HTML5 too packs its share of challenges (<i>some related to screen resolutions too</i>). Let’s take a closer look at those challenges and some solutions too. <br><br>

          <strong>#1- Animations</strong><br><br>
          Undoubtedly when it comes to learning modules animations have become an essential component. HTML5 uses different technology for creating animations and one common challenge here is in getting the audio and video sync right. Creating animations using HTML5 elements (e.g. Canvas, SVG), CSS3, or Spritesheet animations etc. is often more time consuming. However, with the arrival of new development tools (<i>authoring tools</i>) animations in HTML5 poses much less of an issue. <br><br>

          <strong>#2-Code Visibility</strong><br><br>

          Using HTML5 technology as standalone, often leaves traces in terms of cached files on the user machine. This means that the application’s presentation format and even business logic lies exposed. With a little know-how, users can access all JavaScript, CSS, and HTML files and view code too. While certain portions can be masked, trained individuals can easily unmask those. Encryption doesn’t really help to safeguard the HTML5 content, because browsers cannot render that encrypted content.  <br><br>

          Developers minify or obfuscate the code to prevent this. However, using an authoring tool for creating HTML5 content solves this easily, and so does using a Learning management system.<br><br>
        
          <strong>#3- Browsers</strong><br><br>
          Different devices, multiple browsers and the unending permutations of browsers and Operating systems, sounds as a challenge doesn’t it? HTML5 performance hinges greatly on the browsers. So, essentially Chrome, IE 10-11, or Firefox. But, if the organisations insist on older (nn-HTML5) browsers, due to compliance issues, the entire work may be futile. However, MediaElement.js, Modernizr are the development solutions for this issue. <br><br>

          However, it is easier if the developers are aware about the different user-agent strings that will be returned for each targeted device-browser-platform combination, to make things easier.<br><br>

          <strong>#4-Screen Resolutions</strong><br><br>
          HTML5 may have rewritten the way eLearning is viewed on various devices, however getting it right on various devices sizes, screen resolutions and orientations is quite a challenge. Different screen resolutions require different size of images- i.e. higher resolution for desktops and low resolution for mobile devices. However, the use of SVG (scalable vector graphics)- an HTML5 technology allows vector images to be displayed using code, rendering clear image irrespective of the resolution of a user’s screen, quite useful in case of multi device solutions.<br><br>
          
          Different Media formats, HTML5 standards that are constantly upgraded, and cross-device testing etc. pose challenges too, however those issues are quite subjective considering the advancement of authoring tools. The challenges discussed above can even vary from project to project. The key to face the challenges head-on is to observe, take note of the experiences and keep improving. <br><br>
        </p>

