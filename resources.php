          <div class="blogHeading">Quick Tips to Consider Before Starting a Rapid Authoring Project</div>
          <div class="introImage"></div>

          <p>
          Things are fast paced in this millennium, no doubting that! From Content gathering to analysis to development, everything needs to be done in a pace hitherto imagined, all thanks to the increased demand for content and the rapid speed in which it has to be updated. While custom development has its perks, it is relatively time consuming and hence doesn’t always come as a first choice for organisations looking for a quick rollout. <br><br>

          However, as easy as it sounds Rapid authoring too requires through planning to deliver the right outputs. Based on our experience with tool-based, rapid development. Here are some quick tips to consider before getting started with your Rapid Authoring Project.<br><br>

          <ol type="1">
            <li>Invest Time on Finding the Right Authoring Tool <br> Well begun is half-done, and in case of Rapid Authoring the trick lies in choosing the right authoring tool. It is good to try out a few before coming to a conclusion. This allows to not only check the customisability and flexibility that the tool offers, but also analyse the kind of skill-sets or additional expertise required for using the authoring tool to its best. Also, look for additional assets that it offers- image library, animation pre-sets and all before finalising. This process may be time-consuming, but it is better to choose wisely than going for a change after getting started with the project.</li><br><br>
            <li>Look for a Template that can be Reused (customisable too)<br> With the authoring tool finalised and assuming the data gathering, and analysis is in process, choosing a template becomes the priority. Look for a template that can be customised, i.e. image- text space rearrangements, buttons, colours etc. as required. This helps in being future-ready too, as the same template (with branding requirements and all), can be used for any additional course to be created. </li><br><br>
            <li>Gather all the Reusable Content (content repurposing)<br> In cases where new eLearning is being created, it is advisable to look for existing content if any that can be reused, or restructured. Look for images, videos, etc that can be directly used or used in parts, textual content too (PDFs and Word files), lesson plans from Classroom training can be used as raw inputs and for storyboarding. This saves time and also allows to reuse something that the learners are already accustomed to.</li><br><br>
            <li>Create a Good Storyboard (directly importable)<br> With the content and all necessary inputs gathered at one place, the next step is to create a storyboard that matches the requirement. For this a thorough requirement analysis is required which includes not only the learning needs but also clear understanding about the target audience, the kind of learning they are accustomed to, the technical expertise (related to device usage) etc. Some authoring tools even have provision for storyboarding which makes the development process tad bit simpler as the data can be directly imported into the authoring tool and used. </li>
          </ol><br><br>

        <p>  Rapid authoring is cost effective and doesn’t require expert skill-sets for getting started with. Specific customisations however, may require additional coding skills to get the expected output. Even if you are new to the whole concept, the abovementioned tips can give you a head start. <br><br>

