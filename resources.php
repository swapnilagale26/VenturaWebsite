          <div class="blogHeading">Designing Through the Eyes of Learners</div>
          <div class="introImage"></div>
          <p>“Put Yourself in their Shoes.” Heard that one more than once, right? In situations where one fails to interpret other’s thoughts, when one is unable to read between the lines or where one ends up creating something that doesn’t serve the purpose. Well, for those who haven’t. Just ask any Instructional designer. Instructional designers more than often are advised to put themselves in the learner’s shoes, to imagine the learner’s situations, learning environment etc. while giving shape to the learning content. Not, surprising considering the fact that in the end of the day learning/training content is for the learners, designed to meet the learning objectives and organisational goals, that of course includes a skilled workforce.</p><br><br>

          <p>
          We often talk about creating engaging, interactive, intuitive learning, learning that fills the gaps, learning that gives a competitive edge over others etc. But, quite often with all those expectations, the learning ends up incomprehensible, in a form that either scares the learners or is completely unrelatable. And the whole strategy can nearly be unsuccessful, just because of that. <br><br>
          So, ideally every learning design should be carried out keeping the learners in core, or through the eyes of the learners. And how can that be done? Here’s how.<br><br>

          <strong>Identify the Must Have Content</strong><br>
          The design aspect essentially begins with content gathering. From the learner’s perspective, it is often too boring to be introduced to the same concepts and information that they are already aware of. Hence, identifying the level of understanding, existing skill-sets and sorting the information as must-have and additional information helps in keeping the learners interested and the learning goal in place too.<br><br>

          <strong>Determine the Visual Focus</strong><br>
          The crucial aspect of visual design is the focus. Where should the learner focus? What should be retained from the page/course. The elements hence, the images text and buttons should be placed in such a way that the object of most importance catches the attention first. This includes text, colours, typography, illustration style etc. It is easier to consider each page of the course as a poster where all the above-mentioned elements have specific roles and emphasis based on the context. <br><br>

          <strong>Utilise the Power of Images (& media)</strong><br>
          Learners today appreciate the use of visuals instead of text and images are touted to be more powerful in terms of communicating the right message. However, a bit of description can add value to the just images. Infographics, videos on the other hand speak for themselves, and require much less additional (in this case textual) inputs.<br><br>
        
